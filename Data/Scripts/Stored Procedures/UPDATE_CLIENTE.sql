CREATE OR REPLACE PROCEDURE UPDATE_CLIENTE(
	CEDULA_CLIENTE_SP VARCHAR(9),
	NOMBRE_CLIENTE_SP VARCHAR(50),
	APELLIDO1_CLIENTE_SP VARCHAR(50),
	APELLIDO2_CLIENTE_SP VARCHAR(50),
	FECHA_NACIMIENTO_SP TIMESTAMP,
	CORREO_CLIENTE_SP VARCHAR(50),
	CLIENTE_USR_SP VARCHAR(50),
	PASS_CLIENTE_SP VARCHAR(50),
	PROVINCIA_CLIENTE_SP VARCHAR(50),
	CANTON_CLIENTE_SP VARCHAR(50),
	DISTRITO_CLIENTE_SP VARCHAR(50),
	TELEFONO_CLIENTE_SP_1 VARCHAR(8),
	TELEFONO_CLIENTE_SP_2 VARCHAR(8))
	
language plpgsql
as $$
begin
-- stored procedure body
UPDATE CLIENTE
SET
NOMBRE = NOMBRE_CLIENTE_SP,
PRIMER_APELLIDO = APELLIDO1_CLIENTE_SP,
SEGUNDO_APELLIDO = APELLIDO2_CLIENTE_SP,
FECHA_NACIMIENTO = FECHA_NACIMIENTO_SP,
CORREO_ELECTRONICO = CORREO_CLIENTE_SP,
USUARIO_CLIENTE = CLIENTE_USR_SP,
PASSWORD_CLIENTE = PASS_CLIENTE_SP,
PROVINCIA = PROVINCIA_CLIENTE_SP,
CANTON = CANTON_CLIENTE_SP,
DISTRITO = DISTRITO_CLIENTE_SP
WHERE CEDULA_CLIENTE = CEDULA_CLIENTE_SP;

DELETE FROM CLIENTE_TELEFONOS WHERE CEDULA_CLIENTE = CEDULA_CLIENTE_SP;

INSERT INTO CLIENTE_TELEFONOS
VALUES
(DEFAULT,CEDULA_CLIENTE_SP,TELEFONO_CLIENTE_SP_1),
(DEFAULT,CEDULA_CLIENTE_SP,TELEFONO_CLIENTE_SP_2);
end; $$